package at.flockenberger.sirius.engine.animation;

/**
 * <h1>AnimationMode</h1><br>
 * The animation mode describes how the animation is being played.
 * 
 * @author Florian Wagner
 *
 */
public enum AnimationMode
{
	/**
	 * Normal animation.
	 */
	NORMAL,

	/**
	 * Reversed animation.
	 */
	REVERSED,

	/**
	 * Looping animation.
	 */
	LOOP,

	/**
	 * Reversed looping animation.
	 */
	LOOP_REVERSED,
}
